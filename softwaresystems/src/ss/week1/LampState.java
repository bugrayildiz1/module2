/**
 * Enumerates the possible three-way lamp states.
 * @author ciske
 */
public enum LampState {
    /**
     * The off state.
     */
    Off,
    /**
     * The low state.
     */
    Low,
    /**
     * The medium state.
     */
    Medium,
    /**
     * The high state.
     */
    High;

}
